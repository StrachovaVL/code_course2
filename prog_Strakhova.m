% prog_Strakhova.m
% Тема 2. Метод главных компонент (анализ данных вузов)
% Страховая Виктория, группа А-03-22
% Выполнен по аналогии с работой Ауловой А.В.

clear all;
close all;
clc;

disp("=========================================");
disp("Тема 2. Метод главных компонент");
disp("Страховая Виктория, группа А-03-22");
disp("=========================================");

% 1. Загрузка данных из файла
XX = load('dan_vuz.txt');
disp("Размер матрицы XX:");
size(XX)

% 2. Выделение показателей результативности (столбцы 3-13)
X = XX(:, 3:13);
disp("Матрица X (показатели результативности, 11 признаков):");
disp("Размер X:");
size(X)

% 3. Матрица корреляций
R = corr(X);
disp("Матрица корреляций R:");
R

% 4. Метод главных компонент (собственные значения и векторы)
[vect, lambda] = eig(X' * X);
disp("Собственные векторы (vect):");
vect
disp("Собственные значения (lambda):");
lambda

% 5. Выделение собственных значений в вектор
Sobst = diag(lambda);
disp("Собственные значения (в порядке возрастания):");
Sobst

% 6. Вывод собственных значений
fprintf('\n=== СОБСТВЕННЫЕ ЗНАЧЕНИЯ ===\n');
fprintf('Eigenvalues:\n');
for i = 1:length(Sobst)
    fprintf(' %f\n', Sobst(i));
end
fprintf('\n');

% 7. Наибольшее собственное значение и соответствующий вектор
SobMax = Sobst(end);
GlComp = vect(:, end);
fprintf('Наибольшее собственное значение: %f\n', SobMax);
disp("Главная компонента (собственный вектор):");
GlComp

% 8. Доля информации, объясняемая главной компонентой
Delt = 100 * SobMax / sum(Sobst);
fprintf('\n=== ДОЛЯ ИНФОРМАЦИИ ===\n');
fprintf('Delta = %.2f%%\n', Delt);
fprintf('Delta (округленно) = %d%%\n', round(Delt));

% 9. Оценки результативности (главная компонента)
Res = X * GlComp;
disp("Оценки результативности (Res):");
Res

% 10. Вывод результатов с номерами вузов (первый столбец)
fprintf('\n=== РЕЗУЛЬТАТЫ (номер вуза - оценка) ===\n');
fprintf('Results:\n');
for i = 1:length(Res)
    fprintf('%d  %f\n', XX(i, 1), Res(i));
end

% 11. Сохранение результатов в бинарный файл
save res_Strakhova.mat Res -mat;
disp("Результаты сохранены в файл res_Strakhova.mat");

% 12. Построение гистограммы
figure(1);
hist(Res, 20);
xlabel('Results (оценка результативности)');
ylabel('Number of Universities');
title('Гистограмма распределения оценок результативности вузов');
grid on;

% 13. Сохранение гистограммы
saveas(gcf, 'Hist_Strakhova.jpg', 'jpg');
disp("Гистограмма сохранена в файл Hist_Strakhova.jpg");

% 14. Корреляция результативности с финансированием (столбец 2)
CorFin = corr(Res, XX(:, 2));
fprintf('\n=== КОРРЕЛЯЦИЯ ===\n');
fprintf('Correlation of Results and Money = %f\n', CorFin);

disp("=========================================");
disp("Работа выполнена");
disp("=========================================");