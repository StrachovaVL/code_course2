% 3. Замена выбросов
MM_corrected = MM;
for i = 1:5
    for j = 1:7
        if (MM_corrected(i, j) > (SR + 8))
            MM_corrected(i, j) = SR + 8;
        endif
        if (MM_corrected(i, j) < (SR - 8))
            MM_corrected(i, j) = SR - 8;
        endif
    endfor
endfor
